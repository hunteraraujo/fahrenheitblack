from abc import ABC, abstractmethod
from pandas import DataFrame

class StrategyInterface(ABC):
    """
    An abstract base class to define the interface for trading strategies.
    
    Attributes:
        signals (Series): The series of trading signals generated by the strategy.
    """

    def __init__(self):
        """
        The constructor for StrategyInterface class.
        """
        self.signals = None

    @abstractmethod
    def analyze(self, data: DataFrame):
        """
        Analyzes the given market data and generates trading signals.
        
        This method must be implemented by any subclass to define a specific trading strategy.
        
        Parameters:
            data (DataFrame): The most recent market data to analyze.
        
        The implementation should set self.signals to a Series of trading signals.
        """
